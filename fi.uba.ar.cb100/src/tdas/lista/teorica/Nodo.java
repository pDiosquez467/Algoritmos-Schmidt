package tdas.lista.teorica;

public class Nodo<T> {
    //INTERFACES ----------------------------------------------------------------------------------------------
    //ENUMERADOS ----------------------------------------------------------------------------------------------
    //CONSTANTES ----------------------------------------------------------------------------------------------
    //ATRIBUTOS DE CLASE --------------------------------------------------------------------------------------
    //ATRIBUTOS -----------------------------------------------------------------------------------------------

    private T valor;

    //ATRIBUTOS TRANSITORIOS ----------------------------------------------------------------------------------
    //CONSTRUCTORES -------------------------------------------------------------------------------------------

    /**
     * post: Inicializa un nodo con el valor dado.
     * @param valor: el valor que guarda el nodo inicializado.
     */
    public Nodo(T valor) {
        this.valor = valor;
    }

    /**
     * post: Inicializa un nodo con valor null.
     */
    public Nodo() {
        this(null);
    }

    //MÉTODOS ABSTRACTOS --------------------------------------------------------------------------------------
    //MÉTODOS HEREDADOS (CLASE)--------------------------------------------------------------------------------
    //MÉTODOS HEREDADOS (INTERFACE)----------------------------------------------------------------------------
    //MÉTODOS DE CLASE ----------------------------------------------------------------------------------------
    //MÉTODOS GENERALES ---------------------------------------------------------------------------------------
    //MÉTODOS DE COMPORTAMIENTO -------------------------------------------------------------------------------
    //MÉTODOS DE CONSULTA DE ESTADO ---------------------------------------------------------------------------
    //GETTERS REDEFINIDOS -------------------------------------------------------------------------------------
    //GETTERS INICIALIZADOS -----------------------------------------------------------------------------------
    //GETTERS COMPLEJOS ---------------------------------------------------------------------------------------
    //GETTERS SIMPLES -----------------------------------------------------------------------------------------

    /**
     * post: Devuelve el valor que almacena el nodo.
     * @return el valor que almacena el nodo.
     */
    public T getValor() {
        return valor;
    }

    //SETTERS SIMPLES -----------------------------------------------------------------------------------------

    /**
     * post: Modifica el valor almacenado por el nodo.
     * @param valor: el nuevo valor almacenado.
     */
    public void setValor(T valor) {
        this.valor = valor;
    }
}
